package org.tair.module.phyloxml;

public class Events {
    private String speciations;
    private String duplications;

    public String getDuplications() {
        return duplications;
    }

    public String getSpeciations() {
        return speciations;
    }

    public void setDuplications(String duplications) {
        this.duplications = duplications;
    }

    public void setSpeciations(String speciations) {
        this.speciations = speciations;
    }
}
